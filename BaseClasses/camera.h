/*! \file camera.h
 *  \brief Заголовочный файл с описанием интерфейса Camera
 *  \details Содержит объявление абстрактного интерфейса Camera, предоставляющего
 *           минимальный контракт для устройств захвата видео. Интерфейс определяет
 *           метод MakeVideo для симуляции записи видеопотока заданной длительности.
 */
#ifndef CAMERA
#define CAMERA
#include <string>

/*! \class Camera
 *  \brief Абстрактный классы камеры
 *  \details Camera описывает поведение создания видео длительностью seconds для любой камеры,
 *           которое нужно переопределить в зависимости от вида камеры.
 */
class Camera{
public:
    /*! \brief Виртуальный деструктор
     *  \details Обеспечивает корректное уничтожение производных объектов через указатель
     *           на базовый тип Camera.
     */
    virtual ~Camera() = default;

    /*! \brief Записать видео заданной длительности
     *  \param seconds Продолжительность записи в секундах
     *  \return Строковое представление результата записи или статусное сообщение
     *  \details Чисто виртуальный метод, который должен быть реализован в производных классах
     *           для симуляции видеозаписи.
     */
    virtual std::string MakeVideo(int seconds) = 0;
};

#endif